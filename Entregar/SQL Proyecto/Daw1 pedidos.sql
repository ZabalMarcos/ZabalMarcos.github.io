CREATE TABLE PRODUCTOS_PRO(
idproducto number(3) PRIMARY KEY,
nombre varchar2(20),
descripcion varchar2(25),
precio number(6,2),
stock number (3),
categoria varchar2 (20),

);

CREATE TABLE USUARIOS_PRO(
idusuario number(3) PRIMARY KEY,
nombre varchar2(20),
email varchar2(35),
password varchar2 (30),
administrador number (1)
);

CREATE TABLE PEDIDOS_PRO(
IDPEDIDO NUMBER (3) PRIMARY KEY,
FECHAPEDIDO DATE,
IDUSUARIOS NUMBER (3) REFERENCES USUARIOS_PRO
);

CREATE TABLE LINEAS_PEDIDOS_PRO (
IDPEDIDO NUMBER (3) REFERENCES PEDIDOS_PRO,
IDPRODUCTO NUMBER (3) REFERENCES PRODUCTOS_PRO,
CANTIDAD NUMBER (3),
PRECIO NUMBER (6,2),
IMPORTE NUMBER (5,2)
);

